const { contextBridge, ipcRenderer } = require('electron');

const PLATFORM = process.platform;

// Subscribe to an IPC channel; returns an unsubscribe function so React effects
// can clean up. The handler strips the electron `event` arg and spreads the rest,
// preserving whatever arity each callback expects.
const on = (channel, cb) => {
  const handler = (_e, ...args) => cb(...args);
  ipcRenderer.on(channel, handler);
  return () => { ipcRenderer.removeListener(channel, handler); };
};

